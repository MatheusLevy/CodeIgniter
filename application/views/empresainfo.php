        <h2>Históri da Musculação</h2>
        <p>A prática mais utilizada no mundo, que visa o aumento de massa e o condicionamento físico mais maduro, é a musculação. Como prática de ginástica, afirmações históricas podem provar que esta atividade seria realizada desde 400 anos antes de Cristo, contando com relatos de jogos de arremesso de pedras e escavações recentes que encontraram pedras com entalhes para as mãos, associadas prontamente ao treinamento com pesos.

        Como esporte, a atividade se origina também há muito tempo, adaptada como exercício de competição ainda no século XIX. Eugene Sandow, considerado pai da musculação, foi o primeiro homem a tornar do exercício uma prova, tendo como objetivo mostrar que vencia quem tivesse os maiores e melhores músculos. Essa prova londrina teria acontecido só em 1901, onde contava com 156 atletas e teve como vencedor o jovem Willian Murray.</p>
        <h3>Início da referência do esporte</h3>
        <p> cultura física precedida pelo desenvolvimento dos campeonatos de musculação veio a tona com o lançamento da revista “Sandow Magazine”, em 1898, além dos vários livros lançados também por Eugene Sandow que tratavam do esporte, como “Bodybuilding, or Man in the Making”. Sandow inventou aparelhos para a prática e aperfeiçoou outros já existentes, criando cursos de ginastica a partir dos princípios da musculação.

        Além de tudo isso, ainda é considerado o primeiro personal trainer da história, sendo professor particular dos reis Eduardo VII e George V na Inglaterra. Abrindo espaço no mundo fitness que hoje é popular entre jovens e adultos pelo mundo inteiro, Sandow se consagrou como benfeitor da cultura física.</p>
